import turtle
#turtle.setheading(0) # Default is 0, toward East. (0, 360, 720)
turtle.speed(1)
turtle.forward(50)
turtle.setheading(90)
turtle.forward(100)
turtle.setheading(180)
turtle.forward(50)
turtle.setheading(270)
turtle.forward(100)
turtle.done()  # Keep the window open. In terminal don't need this command
 
# Setheading()
# Directly sets the turtle's heading to a specified angle (in degrees).
# The angle is absolute
# 0 degrees points to the right (east).
# 90 degrees points up (north).
# 180 degrees points to the left (west).
# 270 degrees points down (south).